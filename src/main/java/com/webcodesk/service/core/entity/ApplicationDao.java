package com.webcodesk.service.core.entity;

import com.webcodesk.service.core.model.ApplicationVersionBean;
import com.webcodesk.service.core.model.ApplicationVersionBeanDao;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Repository;
import org.springframework.transaction.annotation.Propagation;
import org.springframework.transaction.annotation.Transactional;

@Repository
@Transactional(propagation = Propagation.MANDATORY)
public class ApplicationDao {

    @Autowired
    private ApplicationVersionBeanDao applicationVersionBeanDao;

    public Application getApplication() {
        ApplicationVersionBean version = applicationVersionBeanDao.getLastVersion();
        return Application.newBuilder().withApplicationVersion(version).build();
    }

}
